  private function getWidgetOrgas(Petition $petition) {
    $id = $petition->getId();
    $status = PetitionSigning::STATUS_COUNTED;
    $connection = $this->getConnection();
    $query = "SELECT DISTINCT
                  w.organisation AS organisation
              FROM
                  widget w
              WHERE
                  (w.petition_id = '$id'
                      AND w.organisation != ''
                      AND w.id IN (SELECT
                          *
                      FROM
                          (SELECT
                              p.widget_id AS p__widget_id
                          FROM
                              petition_signing p
                          WHERE
                              p.status = '$status' AND p.widget_id = w.id
                          LIMIT 1) AS t))";
    $statement = $connection->execute($query);
    $statement->execute();
    return $statement->fetchAll(PDO::FETCH_COLUMN, 0);
  }

  private function getWidgetIds(Petition $petition) {
    $id = $petition->getId();
    $status = PetitionSigning::STATUS_COUNTED;
    $connection = $this->getConnection();
    $query = "SELECT
                  w.id AS id
              FROM
                  widget w
              WHERE
                  (w.petition_id = '$id'
                      AND w.id IN (SELECT * FROM (SELECT
                          p.widget_id AS p__widget_id
                      FROM
                          petition_signing p
                      WHERE
                          p.status = '$status' AND p.widget_id = w.id LIMIT 1) as t))
              ORDER BY w.activity_at DESC";
    $statement = $connection->execute($query);
    $statement->execute();
    return $statement->fetchAll(PDO::FETCH_COLUMN, 0);
  }

  private function getWidgetUsers(Petition $petition) {
    $id = $petition->getId();
    $status = PetitionSigning::STATUS_COUNTED;
    $connection = $this->getConnection();
    $query = "SELECT DISTINCT
                  w.user_id AS user_id
              FROM
                  widget w
              WHERE
                  (w.petition_id = '$id'
                      AND w.user_id IS NOT NULL
                      AND w.id IN (SELECT
                          *
                      FROM
                          (SELECT
                              p.widget_id AS p__widget_id
                          FROM
                              petition_signing p
                          WHERE
                              p.status = '$status' AND p.widget_id = w.id
                          LIMIT 1) AS t))
              ORDER BY w.activity_at DESC";
    $statement = $connection->execute($query);
    $statement->execute();
    return $statement->fetchAll(PDO::FETCH_COLUMN, 0);
  }

